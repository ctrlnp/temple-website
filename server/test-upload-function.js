const { uploadImageToCloudinary } = require('./src/config/cloudinary');
const fs = require('fs');
const path = require('path');

async function testUploadFunction() {
  try {
    console.log('Testing uploadImageToCloudinary function...');

    // Create a small test image buffer (1x1 pixel PNG)
    const testImageBuffer = Buffer.from([
      0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A, 0x00, 0x00, 0x00, 0x0D,
      0x49, 0x48, 0x44, 0x52, 0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01,
      0x08, 0x06, 0x00, 0x00, 0x00, 0x1F, 0x15, 0xC4, 0x89, 0x00, 0x00, 0x00,
      0x0B, 0x49, 0x44, 0x41, 0x54, 0x78, 0x9C, 0x63, 0x00, 0x01, 0x00, 0x00,
      0x05, 0x00, 0x01, 0x0D, 0x0A, 0x2D, 0xB4, 0x00, 0x00, 0x00, 0x00, 0x49,
      0x45, 0x4E, 0x44, 0xAE, 0x42, 0x60, 0x82
    ]);

    console.log('Test image buffer size:', testImageBuffer.length, 'bytes');

    const result = await uploadImageToCloudinary(testImageBuffer, 'test.png');

    console.log('✅ Upload successful!');
    console.log('URL:', result.url);
    console.log('Public ID:', result.publicId);

  } catch (error) {
    console.log('❌ Upload failed!');
    console.log('Error:', error.message);
    if (error.response) {
      console.log('Response status:', error.response.status);
      console.log('Response data:', error.response.data);
    }
  }
}

testUploadFunction();
